/**
 * Геометрия дня торговли: где окно фудтрака, где стоит герой и где очередь.
 *
 * Числа сняты с самой сцены, а не выведены из scene-layout.json: у пропса
 * запечён поворот, и «перёд» кузова по локальным осям не угадывается. Мировые
 * габариты food_truck.glb на его месте:
 *
 *   кузов    x 0.39…2.95   z −7.16…−4.35   (ближняя к камере стенка: z = −4.35)
 *   прилавок x 1.64…2.76   y 0.47…0.52
 *
 * Отсюда раздача — по центру прилавка, x ≈ 2.2, у стенки z ≈ −4.35.
 *
 * У кузова нет пола и внутренностей: «внутри» — та же трава. Герой стоит за
 * прилавком так, чтобы доска резала его по пояс, а торс попадал в проём окна.
 * Глубже он повисает над прилавком, ближе — оказывается на улице рядом с
 * очередью и читается как ещё один клиент.
 *
 * Держим числа здесь, а не в компонентах: и Hero, и Customers считают от одной
 * точки, и разъехаться им нельзя.
 */
import * as THREE from 'three'

/** Центр фудтрака на земле. */
export const TRUCK = new THREE.Vector3(1.8441, 0, -5.8892)

/** Центр раздачи по оси кузова: середина прилавка. */
const SERVE_X = 2.45

/** Ближняя к камере стенка кузова — плоскость окна. */
const FACE_Z = -4.35

/** Герой за прилавком, лицом к очереди. */
export const HERO_SEAT = new THREE.Vector3(SERVE_X, 0, FACE_Z - 0.43)

/** Первый в очереди — вплотную к прилавку; остальные за ним, вдоль +Z. */
export const QUEUE_HEAD = new THREE.Vector3(SERVE_X, 0, FACE_Z + 0.72)

/** Шаг между клиентами в очереди. */
export const QUEUE_STEP = 0.85

/** Откуда клиенты приходят: сбоку, из-за деревьев. */
export const SPAWN = new THREE.Vector3(SERVE_X + 4.4, 0, FACE_Z + 2.1)

/** Место i-го в очереди. */
export function queueSpot(i: number, out = new THREE.Vector3()): THREE.Vector3 {
  return out.set(QUEUE_HEAD.x, 0, QUEUE_HEAD.z + i * QUEUE_STEP)
}

/** Куда смотреть, чтобы видеть точку (dx, dz). Модель глядит на −Z. */
export function yawTo(dx: number, dz: number): number {
  return Math.atan2(-dx, -dz)
}
